    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
            try {
                stmt = conn.createStatement();
                int stdId = Integer.parseInt(id.getText());
                String stdName = name.getText();
                String stdBlood = (String) blood.getSelectedItem();
                int stdcls = Integer.parseInt(cls.getText());
                String stdCity = city.getText();
                String stdPhn = phone.getText();
                String stdMail = email.getText();

                String sql = "Insert into stddetails (Id,Student_Name,Blood,Class,Phone,City,mail)"
                + "values('" + stdId + "','" + stdName + "','" + stdBlood + "','" + stdcls + "','" + stdPhn + "','" + stdCity + "','" + stdMail + "')";

                stmt.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data is successfully inserted.");
            } catch (HeadlessException | NumberFormatException | SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        
    }//GEN-LAST:event_jButton1ActionPerformed

